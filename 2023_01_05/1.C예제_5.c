//N까지의 칸이 있고 1부터 N까지의 숫자들이 적혀있다. 
//홀수 칸의 숫자를 지우고 왼쪽으로 반복하다보면 마지막에 하나의 숫자가 남을 것이다 
//그 숫자를 나타내는 프로그램을 작성해라

//입력
   //제약조건
   // N >= && N <=100
//첫번째 줄에 정수 N이 주어진다.
//마지막으로 남은 수를 한 줄에 출력한다.

#include <stdio.h>

int main(void){
	
	int N, Ans = 1;
	printf("몇개의 숫자를 입력할건가요: ");
	scanf("%d", &N);
	if(N>=1 &&N<=100){
		while(1<N){
			N >>=1;
			//마기막에 남음ㄴ 수는 1, 2, 4, 8, 16..등
			//2^n승 값들이다.
			
			Ans <<=1;
		}
		
		printf("%d\n", Ans);
	}
	
	return 0;
	
}